package com.example.firebaseapp01;

public class Student {
    String name;
    String phone;

    public Student() {

    }

    public String getName() {
        return name;
    }

    public String getPhone() {
        return phone;
    }

    public void setName(String name) {
        this.name = name;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }
}
